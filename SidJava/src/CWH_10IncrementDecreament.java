public class CWH_10IncrementDecreament {
    public static void main(String[] args) {
        int a =2;
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a);
        int b =3;
        System.out.println(--b);
        System.out.println(b);
        System.out.println(b--);
        System.out.println(b);
        char ch ='B';
        System.out.println(--ch);
        System.out.println(ch++);
        System.out.println(ch);

    }
}
